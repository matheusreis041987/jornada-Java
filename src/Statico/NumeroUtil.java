package Statico;

public class NumeroUtil {

	public static void compararDoisInts (int int1, int int2) {
		
		if (int1 == int2) {
			System.out.println("Os números são iguais");
		} else if (int1 > int2) {
			System.out.println(int1 + " é maior que " + int2);
		} else if (int1 < int2) {
			System.out.println(int1 + " é menor que " + int2);
		}
		
	}
	
	
}
